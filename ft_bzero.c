#include "libft.h"

void	ft_bzero(void *str, size_t n)
{
	size_t	i;
	char	*s;

	i = 0;
	s = (char *)str;
	while (i < n)
	{
		if (s[i] != '\0')
			s[i] = '\0';
		i++;
	}
}
